#include "push_swap.h"

int		check_arr(int *a, size_t top_a, size_t top_b)
{
	size_t	i;

	if (top_b != 0)
		return (-1);
	i = 0;
	while (i < top_a - 1)
	{
		if (a[i] < a[i + 1])
			return (-1);
		i++;
	}
	return (0);
}
